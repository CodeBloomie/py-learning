
# Operatory porównania

a = 5
b = 5

print(a == 5) # sprawdzamy czy a jest równe 5
print(a != 5) # sprawdzamy czy a jest różne od 5

print(a > 5) # sprawdzamy czy a jest większe od 5
print(a < 5) # sprawdzamy czy a jest mniejsze od 5

print(a >= 5) # sprawdzamy czy a jest większe bądź równe od 5
print(a <= 5) # sprawdzamy czy a jest mniejsze bądź równe od 5